# augmented_assignment

def main():
    x = 100
    print(x)
    x += 10  # augmented assignment addition
    print(x)
    x -= 10  # augmented assignment subtraction
    print(x)
    x *= 10  # augmented assignment multiplication
    print(x)
    x /= 10  # augmented assignment division
    print(x)
    x %= 7  # augmented assignment modulus/remainder
    print(x)
    x //= 3  # augmented assignment integer division
    print(x)


main()
